console.log(1 === "1"); //mais rigido/preciso na comparação.
console.log(1 === true);
console.log(null === undefined);

console.log(1 == "1"); //faz a comparação com menos rigidez, tenta fazer alguma conversão.
console.log(1 == true);
console.log(null == undefined);

console.log(1 !== "1");
console.log(1 !== true);
console.log(null !== undefined);

console.log(1 != "1");
console.log(1 != true);
console.log(null != undefined);